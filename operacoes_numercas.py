# Tipos de numeros que podem ser usados no Python

import math
a = 20
b = 20.5
print(type(a))  # Informa o tipo do valor informado (int)
print(type(b))  # Informa o tipo do valor informado (float)

# Operações Matemáticas
print(10 + 6)
print(10 - 6)
print(10 * 6)
print(10 / 6)
print(10 // 6)  # divisão de inteiro
print(10 % 6)  # modulus informa o resto da divisão
print(10 ** 6)  # exponenciais

# Atalho para atribuição mais rapida
a = 10
a = a + 5
a += 5

b = 20
b = b - 2
b -= 2

# Operações matematicas comuns
print(round(5.7))

# Arredondando o 2.2 para o valor inteiro mais proximo acima
print(math.ceil(2.2))
